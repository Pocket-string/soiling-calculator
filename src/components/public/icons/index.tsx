// Map service icon names to components
export function ServiceIcon({ icon, className }: { icon: string; className?: string }) {
  switch (icon) {
    case 'soiling': return <SolarPanelIcon className={className} />
    case 'irradiance': return <SunIcon className={className} />
    case 'cleaning': return <SparklesIcon className={className} />
    case 'export': return <DocumentIcon className={className} />
    case 'monitoring': return <ChartBarIcon className={className} />
    default: return <SolarPanelIcon className={className} />
  }
}

